package L5Functions;

public class Format {
    public static void main (String [] args)
    {
        System.out.printf("%.2f\n", 1000.0/3.0);
        String name = "Jason";
        int age = 20;
        System.out.printf ("Hello %s, this year, you are %d years old\n", name, age);
        int a = 13;
        System.out.printf ("%x\n", a);
    }
}
