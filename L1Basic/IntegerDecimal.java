package L1Basic;
public class IntegerDecimal {
    public static void main (String [] args)
    {
        int v1 = 9;
        double v2 = 10.0;
        System.out.println ("v1 is originally: " + v1);
        System.out.println ("v2 is originally: " + v2);
        System.out.println();
        v1= v1 / 4;
        v2 = v2 /4;
        System.out.println ("v1 is now: "+ v1);
        System.out.println ("v2 is now: "+ v2);
    }
    
}
